/*	---------------------------------------------------------------------- - */
/*	public関数名	:MaxMotorMeasurement									 */
/*	機能名			:Maxモーター計測										 */
/*	機能概要		:モーターのMaxの値を渡す								 */
/*	引数			:SINT 				: L									 */
/*	戻り値			:int				: FUNC_OK	:関数正常終了			 */
/*					:					: FUNC_ERR	:関数異常終了			 */
/*	作成日			:2018/07/10		 有本　侑真 				 新規作成	 */
/*	-----------------------------------------------------------------------	 */
SINT stMotor_Initialization::MaxMotorMeasurement(I08T *Max)
{
	I08T MaxL = 100;                                      /* 左モーターMax値 */
	I08T MaxR = 100;                                      /* 右モーターMax値 */
	class dcMotor_Output dcM;                        /* dcMotor_Output実体化 */
	dcM.Motor_Output(MaxL,MaxR);
	/* MaxLがMaxRより値が低かった場合                                        */
	if (MaxL<MaxR)
	{
		*Max = MaxL;
	}
	/* MaxRがMaxLより値が低かった場合                                        */
	else if (MaxR<MaxL){
		*Max = MaxR;
	}
	return FUNC_OK;
}
