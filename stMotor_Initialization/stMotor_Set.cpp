SINT stMotor_Initialization::MotorMeasurement(I08T *Max,
	SINT *L,SINT *R,SINT *T)
{
	MaxMotorMeasurement(Max);
	LGearPlayRange(L);
	RGearPlayRange(R);
	TailGearPlayRange(T);
	return FUNC_OK;
}

